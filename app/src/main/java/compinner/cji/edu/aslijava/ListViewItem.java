package compinner.cji.edu.aslijava;

public class ListViewItem {
    private String strMenu;
    private Integer intPrice;
    private Integer intCount;

    public String getStrMenu() {
        return strMenu;
    }

    public void setStrMenu(String strMenu) {
        this.strMenu = strMenu;
    }

    public Integer getIntPrice() {
        return intPrice;
    }

    public void setIntPrice(Integer intPrice) {
        this.intPrice = intPrice;
    }

    public Integer getIntCount() {
        return intCount;
    }

    public void setIntCount(Integer intCount) {
        this.intCount = intCount;
    }
}
